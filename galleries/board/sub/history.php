	<h2 class="htr-contents1-tltie">1940</h2>
	<p class="htr-contents12">롱보드(longboard)는 스케이트보드의 일종으로, 긴 보드이다. <br />
		<span>1940</span>년 후반부터 하와이의 서퍼들이 지상에서도 서핑의 느낌을 
		표현하는 시도가 생겨나면서 스케이트보드가 탄생했으며, 
		<span>1959</span>년 하와이에서 태초 판매용 스케이트보드가 탄생한 것으로
		알려져있다.</p>
