<div class="htr-contents2">
	<div class="htr-contents21"></div>
	<h2 class="htr-contents2-tltie">1959</h2>
	<p class="htr-contents22">연도를 거듭하면서 길이가 짧고 좁은 트릭을 위한 스케이트보드와 길고 넓은 
		부드러운 주행을 위한 롱보드로 나뉘어졌다.<br/>
		스케이트보드와 같이 초기에는 휠의 재질이 유리 섬유 재질로 내구도가 
		약해 안전위험이 발생했다.</p>
</div>
<div class="htr-contents3">
