	<h2 class="htr-contents3-tltie">1973</h2>
	<p class="htr-contents32"><span>1973</span>년에 우레탄 재질의 휠이 출시되면서 안전 및 주행의 불편함이 해결되었고, 
		이후 미국에서 반항적인 라이프스타일로서 선풍적인 인기를 끌게 되었다.
		화려한 묘기가 매력적인 익스트림 스케이트보딩에 묻혀 빛을 보지못하고 있던 
		롱보딩은 기존 롱보드 세대들이 멈추지않고 롱보드를 알리는데 매진했다.</p>
</div>
